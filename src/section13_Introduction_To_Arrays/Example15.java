package section13_Introduction_To_Arrays;

public class Example15
{
    public static void main(String[] args) {
        // Introduction To 2D Arrays
        int[][] nums = null;

        // Creating An Array Which Can Store 3 Array Objects
        nums = new int[3][];

        // Storing An Array Object Of Size 4 At 0th Place
        nums[0] = new int[4];

        // Storing An Array Object Of Size 3 At 1st Index
        nums[1] = new int[3];

        // Storing An Array Object Of Size 6 At 2nd Index
        nums[2] = new int[6];

    }
}
