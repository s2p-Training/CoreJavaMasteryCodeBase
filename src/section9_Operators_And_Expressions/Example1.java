package section9_Operators_And_Expressions;

public class Example1
{
    public static void main(String[] args)
    {
        // Arithmetic Operators
        int a = 10;
        int b = 12;

        int c = a + b;
        System.out.println("A + B : " + c);

        // Reusing Of Variable
        c = a - b;
        System.out.println("A - B : " + c);

        c = a * b;
        System.out.println("A * B : " + c);

        c = 10 / 2;
        System.out.println("10 / 2 : " + c);

        c = 12 % 5;
        System.out.println("12 % 5 : " + c);

    }
}
