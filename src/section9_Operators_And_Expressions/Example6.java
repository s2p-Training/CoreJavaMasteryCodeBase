package section9_Operators_And_Expressions;

public class Example6
{
    public static void main(String[] args) {

        // Static Data Members Of Math Class
        System.out.println("Value Of Exponential : ");
        System.out.println(Math.E);

        System.out.println("Value Of PI : ");
        System.out.println(Math.PI);

        System.out.println("Value Of TAU : ");
        System.out.println(Math.TAU);

        // Static Methods Of Math Class
        // Find The Absolute Value Of A Number
        System.out.println("Abs Value Of -12 : ");
        System.out.println(Math.abs(-12));

        System.out.println("CubeRoot Of 1000 : ");
        System.out.println(Math.cbrt(1000));

        System.out.println("SquareRoot Of 64 : ");
        System.out.println(Math.sqrt(64));

        System.out.println("2 To The Power 5  : ");
        System.out.println(Math.pow(2,5));

        System.out.println("Ceil Value Of 2.56 : ");
        System.out.println(Math.ceil(2.56));

        System.out.println("Ceil Value Of 2.46");
        System.out.println(Math.ceil(2.46));

        System.out.println("Floor Value Of 2.56");
        System.out.println(Math.floor(2.56));

        System.out.println("Log(32) : ");
        System.out.println(Math.log(32));

        System.out.println("Log10(1000) : ");
        System.out.println(Math.log10(1000));

        System.out.println("AdJ:  3  OPPO: 4 : ");
        System.out.println(Math.hypot(3,4));
    }
}
